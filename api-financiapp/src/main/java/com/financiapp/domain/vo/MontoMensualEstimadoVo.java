package com.financiapp.domain.vo;

public class MontoMensualEstimadoVo {

    private String anio;
    private String mes;
    private String montoEstimado;

    public String getAnio() {
        return anio;
    }

    public void setAnio(String anio) {
        this.anio = anio;
    }

    public String getMes() {
        return mes;
    }

    public void setMes(String mes) {
        this.mes = mes;
    }

    public String getMontoEstimado() {
        return montoEstimado;
    }

    public void setMontoEstimado(String monto) {
        this.montoEstimado = monto;
    }

}
