package com.financiapp.domain.vo;

public class SumatoriaGastoMesVo {

    private String gastoTotal;
    private String gastoNecesario;
    private String gastoInnecesario;

    public String getGastoTotal() {
        return gastoTotal;
    }

    public void setGastoTotal(String gastoTotal) {
        this.gastoTotal = gastoTotal;
    }

    public String getGastoNecesario() {
        return gastoNecesario;
    }

    public void setGastoNecesario(String gastoNecesario) {
        this.gastoNecesario = gastoNecesario;
    }

    public String getGastoInnecesario() {
        return gastoInnecesario;
    }

    public void setGastoInnecesario(String gastoInnecesario) {
        this.gastoInnecesario = gastoInnecesario;
    }

}
